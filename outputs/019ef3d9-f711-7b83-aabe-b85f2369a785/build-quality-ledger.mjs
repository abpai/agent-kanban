function writeTable(sheet, startCell, headers, rows, tableName) {
  const matrix = [headers, ...rows]
  const range = sheet.getRangeByIndexes(0, 0, matrix.length, headers.length)
  range.values = matrix
  range.format = { wrapText: true }
  sheet.getRangeByIndexes(0, 0, 1, headers.length).format = {
    fill: '#1F4E78',
    font: { bold: true, color: '#FFFFFF' },
    wrapText: true,
  }
  sheet.getRangeByIndexes(0, 0, matrix.length, headers.length).format.borders = {
    preset: 'outside',
    style: 'thin',
    color: '#A6A6A6',
  }
  sheet.tables.add(startCell, true, tableName)
  sheet.freezePanes.freezeRows(1)
  sheet.showGridLines = false
}

